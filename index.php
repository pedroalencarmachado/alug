        function calcula_preco_diaria(){
          var dt1 = $('#dtini').val();
          var dt2 = $('#dtfim').val();
          
          var preco_diaria_produto = $('#preco_produto').text();
          var qtd_dias = calcula_dif_dias(dt1,dt2);

          var lbl_diaria;
          var lbl_desconto;
          var lbl_total;

          if(qtd_dias != 3 && qtd_dias != 5 && qtd_dias != 7 && qtd_dias != 10 && qtd_dias !=15 && qtd_dias <15){
            $('#div_produto_obs').show();
          } else {
            $('#div_produto_obs').hide();
          }

          if(qtd_dias ===0 || isNaN(qtd_dias) || dt1 > dt2){
            lbl_diaria = preco_diaria_produto;
            lbl_desconto = "-0%";
            lbl_total = preco_diaria_produto;
          }else if(qtd_dias > 0 && qtd_dias < 3){
            lbl_diaria = preco_diaria_produto;
            lbl_desconto = "-0%";
            lbl_total = preco_diaria_produto * qtd_dias;
          }else if(qtd_dias >= 3 && qtd_dias < 5){
            //10%            
            lbl_desconto = "-10%";
            lbl_total = (preco_diaria_produto * qtd_dias) * 0.9;
            lbl_diaria = lbl_total / qtd_dias;
          } else if( qtd_dias >= 5 && qtd_dias < 7){
            //15%            
            lbl_desconto = "-15%";
            lbl_total = (preco_diaria_produto * qtd_dias) * 0.85;
            lbl_diaria = lbl_total / qtd_dias;
          } else if( qtd_dias >= 7 && qtd_dias < 10){
            //20%            
            lbl_desconto = "-20%";
            lbl_total = (preco_diaria_produto * qtd_dias) * 0.8;
            lbl_diaria = lbl_total / qtd_dias;
          } else if( qtd_dias >= 10 && qtd_dias < 15){
            //40%            
            lbl_desconto = "-40%";
            lbl_total = (preco_diaria_produto * qtd_dias) * 0.6;
            lbl_diaria = lbl_total / qtd_dias;
          } else if( qtd_dias >= 15){
            //50%            
            lbl_desconto = "-50%";
            lbl_total = (preco_diaria_produto * qtd_dias) * 0.5;
            lbl_diaria = lbl_total / qtd_dias;
          }

          if(lbl_desconto==="-0%"){
            $('#produto_diaria').html("R$: "+lbl_diaria+"/dia"); 
            $('#produto_desconto_diaria').html(lbl_desconto);
            $('#produto_total').html("Total R$: "+lbl_total); 
          }else{      
            $('#produto_diaria').html("R$: "+Math.ceil(lbl_diaria)+",00 /dia");  
            $('#produto_desconto_diaria').html(lbl_desconto);
            $('#produto_total').html("Total R$: "+Math.ceil(lbl_total)+ ",00");
          }
          //alert(calcula_dif_dias(dt1,dt2));

        };

